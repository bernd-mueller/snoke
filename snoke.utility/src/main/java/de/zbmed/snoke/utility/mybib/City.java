package de.zbmed.snoke.utility.mybib;

public class City {
	String stadt, plz, bland, kreis, key;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getStadt() {
		return stadt;
	}

	public void setStadt(String stadt) {
		this.stadt = stadt;
	}

	public String getPlz() {
		return plz;
	}

	public void setPlz(String plz) {
		this.plz = plz;
	}

	public String getBland() {
		return bland;
	}

	public void setBland(String bland) {
		this.bland = bland;
	}

	public String getKreis() {
		return kreis;
	}

	public void setKreis(String kreis) {
		this.kreis = kreis;
	}
}
